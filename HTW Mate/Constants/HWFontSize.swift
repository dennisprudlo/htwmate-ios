import Foundation
import CoreGraphics

struct HWFontSize {

    /// The font size fot a dashboard section title of 24pt
    static let sectionTitle: CGFloat = 28;

    /// The font size for a detail view headline of 27pt
    static let title: CGFloat = 27;

    /// The font size for a small label of 10pt
    static let label: CGFloat = 10;

    /// The font size for a meta information of 12pt
    static let metaInfo: CGFloat = 12;

    /// The font size for a strong highlighted text of 15pt
    static let strongText: CGFloat = 15;

    /// The font size for an enlarged highlighted text of 17pt
    static let enlargedText: CGFloat = 17;

    /// The font size for a paragraph text of 13pt
    static let text: CGFloat = 13;
}
